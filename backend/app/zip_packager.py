def create_zip(
    code_output: str,
    qa_summary: str,
    goal_analysis: str,
    project_name: str = "devcrew_project",
    chosen_stack: str = "crewai"
) -> str:
    files = parse_files_from_output(code_output)

    if not files:
        files['README.md'] = (
            f"# {project_name}\n\n"
            f"## Project Summary\n{goal_analysis}\n\n"
            f"## Note\n"
            f"The code generation did not produce parseable files. "
            f"Please try again.\n\n"
            f"## QA Summary\n{qa_summary}"
        )

    files = ensure_readme(files, qa_summary, goal_analysis, chosen_stack)
    files = ensure_env_example(files, chosen_stack)

    tmp_dir = tempfile.mkdtemp()
    zip_path = os.path.join(tmp_dir, f"{project_name}.zip")

    with zipfile.ZipFile(zip_path, 'w', zipfile.ZIP_DEFLATED) as zf:
        for filepath, content in files.items():
            arcname = os.path.join(project_name, filepath)
            arcname = arcname.replace('\\', '/')
            zf.writestr(arcname, content)

        build_log = f"""# DevCrew Build Log

## Stack Used
{chosen_stack}

## QA Summary
{qa_summary}

## Files Generated
{chr(10).join(f'- {f}' for f in files.keys())}

## Built With
DevCrew — AI-powered project builder
Powered by CrewAI + Groq
"""
        zf.writestr(
            os.path.join(project_name, 'DEVCREW_BUILD_LOG.md'),
            build_log
        )

    return zip_path


def get_project_name(goal_analysis: str) -> str:
    lines = goal_analysis.split('\n')
    for line in lines:
        if 'project summary' in line.lower() or 'project:' in line.lower():
            clean = re.sub(r'[^a-zA-Z0-9\s]', '', line)
            words = clean.strip().split()[:4]
            if words:
                return '_'.join(words).lower()
    return 'devcrew_project'



