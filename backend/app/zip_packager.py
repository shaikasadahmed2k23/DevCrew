import os
import re
import zipfile
import tempfile
from pathlib import Path


def parse_files_from_output(code_output: str) -> dict:
    files = {}
    pattern = r'#\s*FILE:\s*([^\n]+)\n(.*?)(?=\n#\s*FILE:|\Z)'
    matches = re.findall(pattern, code_output, re.DOTALL)

    for filepath, content in matches:
        filepath = filepath.strip()
        content = content.strip()
        content = re.sub(r'^```[\w]*\n?', '', content)
        content = re.sub(r'\n?```$', '', content)
        content = content.strip()
        files[filepath] = content

    return files


def ensure_readme(
    files: dict,
    qa_summary: str,
    goal_analysis: str,
    chosen_stack: str = "crewai"
) -> dict:
    has_readme = any(
        k.lower() == 'readme.md' or k.lower().endswith('/readme.md')
        for k in files.keys()
    )

    if not has_readme:
        stack_line = f"- Generated by DevCrew — AI-powered project builder"
        if chosen_stack.lower() == "crewai":
            built_with = "CrewAI + Groq"
        elif chosen_stack.lower() == "langchain":
            built_with = "LangChain + Groq"
        elif chosen_stack.lower() == "nodejs":
            built_with = "Node.js + Groq"
        elif chosen_stack.lower() == "autogen":
            built_with = "AutoGen + Groq"
        else:
            built_with = f"{chosen_stack} + Groq"

        files['README.md'] = f"""# DevCrew Generated Project

## Project Summary
{goal_analysis}

## Setup Instructions
1. Clone or extract this project folder
2. Install dependencies as listed below
3. Copy `.env.example` to `.env` and fill in your credentials
4. Run the project as described in the How to Run section

## Credentials
- Check `.env.example` for all required environment variables
- Each variable has a comment explaining where to get it

## How to Run
- Follow the setup instructions above
- Refer to individual file comments for more detail

## Built With
{stack_line}
- Powered by {built_with}

---
## QA Summary
{qa_summary}

---
*This project was generated by DevCrew. Replace all placeholder values before running.*
"""
    else:
        for key in list(files.keys()):
            if key.lower() == 'readme.md' or key.lower().endswith('/readme.md'):
                content = files[key]
                if "crewai + groq" in content.lower() and chosen_stack.lower() != "crewai":
                    if chosen_stack.lower() == "langchain":
                        files[key] = content.replace(
                            "CrewAI + Groq", "LangChain + Groq"
                        ).replace(
                            "crewai + groq", "LangChain + Groq"
                        )
                    else:
                        files[key] = content.replace(
                            "CrewAI + Groq", f"{chosen_stack} + Groq"
                        )
                break

    return files


